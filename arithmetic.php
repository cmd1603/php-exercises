<?php

function add($a, $b)
{
    return $a + $b;
}
echo add(5, 4). PHP_EOL;

function subtract($a, $b)
{
    return $a - $b;

}
echo subtract(5, 4). PHP_EOL;

function multiply($a, $b)
{
    return $a * $b;

}
echo multiply(5, 4). PHP_EOL;

function divide($a, $b)
{
    return $a / $b;

}
echo divide(20, 4). PHP_EOL;

function modulus($a, $b)
{
	return $a % $b;
}
echo modulus(10, 3). PHP_EOL;